class ConstantDemo {
    public static void main (String[] args) {
        //字符串常量
        System.out.println("Hello World!");

        //整数常量
        System.out.println(23);

        //小数常量
        System.out.println(1.23);

        //字符常量
        System.out.println('A');
        System.out.println('1');

        //布尔常量常量
        System.out.println(true);
        System.out.println(false);
    }
}