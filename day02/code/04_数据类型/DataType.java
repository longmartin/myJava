class DataType {
    public static void main (String[] args) {
        //定义byte类型
        byte b = 1;
        System.out.println(b);

        //定义short类型
        short s = 100;
        System.out.println(s);

        //定义int类型
        int i = 3213;
        System.out.println(i);

        //定义long类型
        long l = 2147483648L;
        System.out.println(l);

        //定义float类型
        float f = 2.14F;
        System.out.println(f);

        //定义double类型
        double d = 2.13;
        System.out.println(d);

        //定义char类型
        char c = 'A';
        System.out.println(c);

        //定义boolean类型
        boolean flag = true;
        System.out.println(flag);
    }
}